package tipoPersonagens;

public class HeroiOuVialao {

	private String heroiOuVilao;
	private String poderes;
	private String inimigos;
	private String nivelPoder;
	
	
	public String getHeroiOuVilao() {
		return heroiOuVilao;
	}
	public void setHeroiOuVilao(String heroiOuVilao) {
		this.heroiOuVilao = heroiOuVilao;
	}
	public String getPoderes() {
		return poderes;
	}
	public void setPoderes(String poderes) {
		this.poderes = poderes;
	}
	public String getInimigos() {
		return inimigos;
	}
	public void setInimigos(String inimigos) {
		this.inimigos = inimigos;
	}
	public String getNivelPoder() {
		return nivelPoder;
	}
	public void setNivelPoder(String nivelPoder) {
		this.nivelPoder = nivelPoder;
	}
	
}